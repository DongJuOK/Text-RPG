int Fight_Select()
{
	int x = 28;
	int y = 10;

	gotoxy(x, y);
	printf(">");

	while (1)
	{
		if (_kbhit())
		{
			key = _getch();

			if (key == -32)
			{
				key = _getch();
			}

			switch (key)
			{
			case DOWN: if (y < 15)
			{
				gotoxy(x, y);
				printf("  ");
				y += 5;
				gotoxy(x, y);
				printf(">");
			}
				break;

			case UP: if (y > 10)
			{
				gotoxy(x, y);
				printf("  ");
				y -= 5;
				gotoxy(x, y);
				printf(">");
			}
				break;

			case SUBMIT: 
			{
			    if (y == 15)
			    {
			    	if (s == 0)
			    	{
						zombie1.health = 100;
						zombie2.health = 80;
						zombie3.health = 65;
			    		c_box();
			    		data_shovel();
			    	}
			    	else if (s == 1)
			    	{
						zombie1.health = 100;
						zombie2.health = 80;
						zombie3.health = 65;
			    		c_box();
			    		data_lighter();
			    	}
			    	else if (s == 2)
			    	{
						zombie1.health = 100;
						zombie2.health = 80;
						zombie3.health = 65;
			    		c_box();
			    		data_gun();
			    	}
			    }

				if (y == 10)
				{
					Fight_System();
				}
			}
			    return 0;
			}
		}
	}

	return 0;
}

