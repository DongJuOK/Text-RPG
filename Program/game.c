int menu()
{
	int x = 32;
	int y = 14;

	gotoxy(x - 2, y);
	printf("> 게 임 시 작");
	gotoxy(x, y + 2);
	printf("게 임 정 보");
	gotoxy(x, y + 4);
	printf("   종 료   ");

	while (1)
	{
		if (_kbhit())
		{
			key = _getch();

			if (key == -32)
			{
				key = _getch();
			}

			switch (key)
			{
			case UP: if (y > 14)
			{
				gotoxy(x - 2, y);
				printf(" ");
				gotoxy(x - 2, y -= 2);
				printf(">");
			}
				   break;

			case DOWN: if (y < 18)
			{
				gotoxy(x - 2, y);
				printf(" ");
				gotoxy(x - 2, y += 2);
				printf(">");
			}
					 break;

			case SUBMIT:
			{
				return y - 14;
			}
			}
		}
	}

	return 0;
}

